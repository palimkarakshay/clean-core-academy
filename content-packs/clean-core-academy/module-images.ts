export function moduleImageUrl(sectionId: string): string | undefined {
  const art = MODULE_ART[Object.hasOwn(MODULE_ART, sectionId) ? sectionId : ""];
  return art ? pollinationsUrl(art.prompt, art.seed) : undefined;
}

/**
 * Attach generated thumbnails to sections without mutating the source
 * module objects. A section that already carries an `iconImagePath`
 * keeps it; one with no mapped art is returned unchanged.
 */
export function withModuleImages(sections: Section[]): Section[] {
  return sections.map((s) => {
    if (s.iconImagePath) return s;
    const url = moduleImageUrl(s.id);
    return url ? { ...s, iconImagePath: url } : s;
  });
}
