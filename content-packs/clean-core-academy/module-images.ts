/* ------------------------------------------------------------------
   Module + hero artwork, generated by Pollinations AI.

   We store *prompts and seeds* rather than committing binary files, and
   build deterministic Pollinations image URLs at module-load time. A
   fixed seed per image keeps it stable across renders and builds, while
   the repo stays lean and the art is always regenerable by editing a
   prompt. Images are served remotely (image.pollinations.ai is allowed
   in next.config.ts `images.remotePatterns` and by the CSP img-src),
   so no build-time fetch is required.

   A shared STYLE suffix pins the palette to the pack theme (deep navy
   #0a2540, teal #0a6e8a / #1aa899) so every tile feels like one set.
------------------------------------------------------------------ */

import type { Section } from "./_types";

const STYLE =
  "isometric flat vector illustration, deep navy blue and teal colour palette, clean minimal corporate style, soft studio lighting, plain off-white background, no text, no words, no letters";

/** Build a deterministic Pollinations image URL for a prompt + seed. */
export function pollinationsUrl(
  prompt: string,
  seed: number,
  width = 480,
  height = 480
): string {
  const full = `${prompt}, ${STYLE}`;
  return (
    `https://image.pollinations.ai/prompt/${encodeURIComponent(full)}` +
    `?width=${width}&height=${height}&seed=${seed}&nologo=true&model=flux`
  );
}

